"""Calendar task functions."""

import random
import uuid
from datetime import date, timedelta

from conftest import (
    CALENDAR_ME,
    CALENDAR_EVENTS,
    CALENDAR_EVENTS_CREATE,
    CALENDAR_CATEGORIES,
    CALENDAR_CATEGORY_IDS,
)


def get_my_calendar(client) -> None:
    """GET /api/calendar/me/."""
    with client.get(
        CALENDAR_ME,
        name="[calendar] get calendar",
        catch_response=True,
    ) as resp:
        if resp.status_code == 200:
            return
        resp.failure(f"calendar/me failed: {resp.status_code}")


